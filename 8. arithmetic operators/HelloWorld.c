#include <stdio.h>

int main() {
    // arithmetic operators = perform arithmetic on variables and constants

    // + = addition
    // - = subtraction
    // * = multiplication
    // / = division
    // % = modulus (remainder)
    // ++ = increment
    // -- = decrement

    int x = 5;
    int y = 2;

    x++; // x = x + 1
    y--; // y = y - 1

    printf("Calculation of %d and %d\n", x, y);

}